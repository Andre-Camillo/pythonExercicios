expr = str(input('DIGITE SUA EXPRESSÃO: '))
pilha = list()
for simb in expr:
    if simb == '(':
        pilha.append('(')
    elif simb == ')':
        if len(pilha) > 0:
            pilha.pop()
        else:
            pilha.append(')')
            break
if len(pilha) == 0:
    print('A EXPRESSÃO ESTÁ CORRETA.')
else:
    print('A EXPRESSÃO ESTÁ INCORRETA.')
